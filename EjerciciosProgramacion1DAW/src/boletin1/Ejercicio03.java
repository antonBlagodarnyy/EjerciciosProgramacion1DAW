package boletin1;

public class Ejercicio03 {
	public static void main(String[] args) {
		/*
		 * Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
		 * correspondiente traducción al castellano. Las palabras deben estar
		 * distribuidas en dos columnas y alineadas a la izquierda. Pista: Se puede
		 * insertar un tabulador mediante \t.
		 */

		System.out.printf("%-10s", "house");
		System.out.println("casa");

		System.out.printf("%-10s", "car");
		System.out.println("coche");

		System.out.printf("%-10s", "cat");
		System.out.println("gato");

		System.out.printf("%-10s", "money");
		System.out.println("dinero");

		System.out.printf("%-10s", "book");
		System.out.println("libro");

		System.out.printf("%-10s", "lion");
		System.out.println("leon");

		System.out.printf("%-10s", "rocket");
		System.out.println("cohete");

		System.out.printf("%-10s", "sell");
		System.out.println("vender");

		System.out.printf("%-10s", "lamp");
		System.out.println("lampara");

		System.out.printf("%-10s", "bed");
		System.out.println("cama");

	}
}
